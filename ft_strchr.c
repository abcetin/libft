#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s1;

	s1 = (char *)s;
	while (*s1 != '\0' && (char)c != *s1)
		s1++;
	if ((char)c == *s1)
		return (s1);
	return (0);
}
