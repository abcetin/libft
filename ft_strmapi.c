#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*ret;
	unsigned int	i;
	int				len;

	i = 0;
	if (!s)
		return (0);
	len = ft_strlen(s);
	ret = (char *)malloc(sizeof(char) * len + 1);
	if (!ret)
		return (0);
	while (s[i] != '\0')
	{
		ret[i] = (*f)(i, s[i]);
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
